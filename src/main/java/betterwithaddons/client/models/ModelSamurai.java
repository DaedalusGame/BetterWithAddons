    @Override
    public void render(Entity entity, float limbSwing, float limbSwingAmount, float ageInTicks, float netHeadYaw, float headPitch, float scale) {
        if(entity instanceof EntityArmorStand) {
            // Hack so helmets look right on armor stand
            netHeadYaw = 0;
        }

        headOuter.showModel = slot == EntityEquipmentSlot.HEAD;
        body.showModel = slot == EntityEquipmentSlot.CHEST;
        armR.showModel = slot == EntityEquipmentSlot.CHEST;
        armL.showModel = slot == EntityEquipmentSlot.CHEST;
        skirt.showModel = slot == EntityEquipmentSlot.LEGS;
        hideLeg(legR,slot);
        hideLeg(legL,slot);

        bipedHeadwear.showModel = false;

        bipedHead = headOuter;
        bipedBody = slot == EntityEquipmentSlot.LEGS ? skirt : body;
        bipedRightArm = armR;
        bipedLeftArm = armL;
        bipedRightLeg = legR;
        bipedLeftLeg = legL;

        super.render(entity,limbSwing,limbSwingAmount,ageInTicks,netHeadYaw,headPitch,scale);
    }

    public void hideLeg(ModelRenderer leg, EntityEquipmentSlot slot)
    {
        leg.showModel = slot == EntityEquipmentSlot.LEGS || slot == EntityEquipmentSlot.FEET;
        for (ModelRenderer child : leg.childModels) {
            child.showModel = slot == EntityEquipmentSlot.FEET;
        }
    }

    public void setRotateAngle(ModelRenderer modelRenderer, float x, float y, float z) {
        modelRenderer.rotateAngleX = x;
        modelRenderer.rotateAngleY = y;
        modelRenderer.rotateAngleZ = z;
    }
